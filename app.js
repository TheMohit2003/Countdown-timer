// the following function gives one digit number a tenth place of zero . eg 0 -> 00 , 1->01 etc 

function digitConvertor(number) {
    switch (number) {
        case 0:
            return parseInt("00");
            break;
        case 1:
            return parseInt("01");
            break;
        case 2:
            return parseInt("02");
            break;
        case 3:
            return parseInt("03");
            break;
        case 4:
            return parseInt("04");
            break;
        case 5:
            return parseInt("05");
            break;
        case 6:
            return parseInt("06");
            break;
        case 7:
            return parseInt("07");
            break;
        case 8:
            return parseInt("08");
            break;
        case 9:
            return parseInt("09");
    }
}



